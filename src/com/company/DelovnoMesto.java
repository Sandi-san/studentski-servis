package com.company;

public class DelovnoMesto {
    public String Naziv;
    public String Opis;
    public String Placa;
    public String Trajanje;
    public String Delovnik;
    public String Sifra;
    public int Prosto;
    public String Kraj;
    public String Podjetje;
    public String FilePath;

    public DelovnoMesto(String naziv, String opis, String placa, String trajanje, String delovnik, String sifra, int prosto, String kraj, String podjetje, String filepath){
        Naziv = naziv;
        Opis = opis;
        Placa = placa;
        Trajanje = trajanje;
        Delovnik = delovnik;
        Sifra = sifra;
        Prosto = prosto;
        Kraj = kraj;
        Podjetje = podjetje;
        FilePath = filepath;
    }
}


